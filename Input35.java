import java.util.*;
public class Input35 {
    public static void main(String[] args) {
        int n=1;
        while(n<=100){
            if(n%2==0){
                System.out.println(n+" is even number");
            }
            else{
                System.out.println(n+" is odd number");
            }
            n++;
        }
    }
}
