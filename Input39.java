import java.util.*;
public class Input39 {
    public static void main(String[] args) {
        int n=2;
        while(n<=20){
            int i=1;
            while(i<=10){
                System.out.println(n+"*"+i+"="+n*i);
                i++;
            }
            System.out.println();
            n++;
        }
    }
}
